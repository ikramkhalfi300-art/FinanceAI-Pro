try:
    pdfmetrics.registerFont(TTFont('ArabicFont', 'Arial.ttf'))
except Exception:
    pdfmetrics.registerFont(TTFont('ArabicFont', 'Helvetica'))


def format_arabic(text: str) -> str:
    if not text:
        return ""
    reshaped = arabic_reshaper.reshape(text)
    return get_display(reshaped)


def generate_pdf_ar(analysis: str, currency: str, language: str) -> bytes:
    buffer = io.BytesIO()
    doc = SimpleDocTemplate(buffer, pagesize=A4,
                           rightMargin=50, leftMargin=50,
                           topMargin=50, bottomMargin=50)

    title_text = format_arabic("تقرير مالي شامل")

